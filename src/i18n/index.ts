let currentLocale: "ar" | "en" = "ar";

export const setLocale = (l: "ar" | "en") => { currentLocale = l; };
export const getLocale = () => currentLocale;
export const t = (key: string, params?: Record<string, string | number>) => {
  const template = (translations[currentLocale] as any)[key] || key;
  if (!params) return template;
  return String(template).replace(/\{\{(\w+)\}\}/g, (_, p1) => {
    const v = (params as any)[p1];
    return v == null ? '' : String(v);
  });
};
export const isRTL = () => currentLocale === "ar";
